    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
       
        if (txtnumero.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un número de habitación");
            txtnumero.requestFocus();
            return;
        }
        
        if (cbpiso.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un piso.");
            cboestado.requestFocus();
            return;
        }
        
        if (txtdescripcion.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar una descripción.");
            txtdescripcion.requestFocus();
            return;
        }

        if (txtcaracteristicas.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar las características.");
            txtcaracteristicas.requestFocus();
            return;
        }

        if (txtprecio.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el precio diario.");
            txtprecio.requestFocus();
            return;
        }

        try {
            double precio = Double.parseDouble(txtprecio.getText());
            if (precio <= 0) {
                JOptionPane.showMessageDialog(rootPane, "El precio debe ser mayor a 0.");
                txtprecio.requestFocus();
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un valor numérico válido para el precio.");
            txtprecio.requestFocus();
            return;
        }
        
        if (cboestado.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un estado.");
            cboestado.requestFocus();
            return;
        }

        if (cbotipohabitacion.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un tipo de habitación.");
            cbotipohabitacion.requestFocus();
            return;
        }


        vhabitacion dts = new vhabitacion();
        fhabitacion func = new fhabitacion();

        dts.setNumero(txtnumero.getText());
        dts.setPiso(cbpiso.getSelectedItem().toString());
        dts.setDescripcion(txtdescripcion.getText());
        dts.setCaracteristicas(txtcaracteristicas.getText());
        dts.setPrecio_diario(txtprecio.getText());
        dts.setEstado(cboestado.getSelectedItem().toString());
        dts.setTipo_habitacion(cbotipohabitacion.getSelectedItem().toString());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Habitación registrada correctamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIdhabitacion(Integer.parseInt(txthabitacion.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Habitación actualizada correctamente");
                mostrar("");
                inhabilitar();
            }
        }
           
        
    }//GEN-LAST:event_btnguardarActionPerformed

