    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        
        if (!txthabitacion.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de eliminar la habitación?", "Confirmar", 2);

            if (confirmacion == 0) {
                fhabitacion func = new fhabitacion();
                vhabitacion dts = new vhabitacion();

                dts.setIdhabitacion(Integer.parseInt(txthabitacion.getText()));
                dts.setNumero(txtnumero.getText());

                if (func.eliminar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "La habitación fue eliminada correctamente.");
                    mostrar("");
                    inhabilitar();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecciona una habitación primero.");
        }
    }//GEN-LAST:event_btneliminarActionPerformed

